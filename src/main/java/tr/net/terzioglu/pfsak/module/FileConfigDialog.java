    void setFileConfig(FileConfig aThis) {

        fc = aThis;

        jComboBox1.setSelectedIndex(fc.getFileType().ordinal());

        if (fc.getFileName() != null) {
            jTextField1.setText(fc.getFileName());
        }
    }

